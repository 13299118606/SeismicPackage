package com.conoco.cfe.server.messaging;

/**
 * An interface to define some messaging constants.
 */
public interface MessagingConstants {
  
  /**
   * A static constant for the window id attribute
   * 
   * @serial
   */
  public final static String WINDOW_ID    = "windowId";
  
  /**
   * A static constant for the keyword attribute
   * 
   * @serial
   */
  public final static String KEYWORD      = "keyword";
  
  /**
   * A static constant for the elements attribute
   * 
   * @serial
   */
  public final static String ELEMENTS      = "elements";
  
  /**
   * A static constant for the start element attribute
   * 
   * @serial
   */
  public final static String START_ELEMENT  = "startElement";
  
  /**
   * A static constant for the end element attribute
   * 
   * @serial
   */
  public final static String END_ELEMENT    = "endElement";
  
  /**
   * A static constant for the element index attribute
   * 
   * @serial 
   */
  public final static String ELEMENT_INDEX  = "elementIndex";
  
  /**
   * A static constant for the value attribute
   *
   * @serial
   */
  public final static String VALUE      = "value";
  
  
}